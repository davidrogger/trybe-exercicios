function basketFruitQuantity (basket){
  const fruitList = {};
  let finalReport = [];
  for (let indexMain = 0; indexMain < basket.length; indexMain += 1){
    let qtFruit = 0;
    for (let indexCheck = 0; indexCheck < basket.length; indexCheck += 1){
      if (basket[indexMain] == basket[indexCheck]){
        qtFruit += 1;
      }      
    }
    fruitList[basket[indexMain]] = qtFruit;
  }
  for(let basketReport in fruitList){
    finalReport.push(`${fruitList[basketReport]} ${basketReport}s,`)
  }
  let joinFinalReport = finalReport.join(' ');
  let finalReportMsg = 'Sua cesta possui: '+ joinFinalReport;
  return finalReportMsg;
}

